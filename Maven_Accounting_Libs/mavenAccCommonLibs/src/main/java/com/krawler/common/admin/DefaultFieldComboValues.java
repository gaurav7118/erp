public class DefaultFieldComboValues {

    private String ID;
    private DefaultCustomFields customFields;
    private String fieldComboValue;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public DefaultCustomFields getCustomFields() {
        return customFields;
    }

    public void setCustomFields(DefaultCustomFields customFields) {
        this.customFields = customFields;
    }

    public String getFieldComboValue() {
        return fieldComboValue;
    }

    public void setFieldComboValue(String fieldComboValue) {
        this.fieldComboValue = fieldComboValue;
    }

}
