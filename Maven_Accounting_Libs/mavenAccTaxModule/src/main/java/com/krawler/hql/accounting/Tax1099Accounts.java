package com.krawler.hql.accounting;

import com.krawler.common.admin.Company;

/**
 *
 * @author krawler
 */
public class Tax1099Accounts {

    private String ID;
    private Tax1099Category tax1099Category;
    private Account account;
    private boolean deleted;
    private Company company;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public Account getAccount() {
        return account;
    }

    public void setAccount(Account account) {
        this.account = account;
    }

    public Company getCompany() {
        return company;
    }

    public void setCompany(Company company) {
        this.company = company;
    }

    public boolean isDeleted() {
        return deleted;
    }

    public void setDeleted(boolean deleted) {
        this.deleted = deleted;
    }

    public Tax1099Category getTax1099Category() {
        return tax1099Category;
    }

    public void setTax1099Category(Tax1099Category tax1099Category) {
        this.tax1099Category = tax1099Category;
    }
}
